import numpy as np

t1 = np.linspace(5, 25, 5)
print(t1, '\n ------------------------------------------------------------')
t2 = np.linspace(2, 30, 3)
print(t2, '\n ------------------------------------------------------------')
x1, x2 = np.meshgrid(t1, t2)  # 生成网格采样点
print(x1, '\n ------------------------------------------------------------')
print(x2, '\n ------------------------------------------------------------')
print((x1.flat, x2.flat), '\n ------------------------------------------------------------')

x_show = np.stack((x1.flat, x2.flat), axis=1)  # 测试点
print(x_show, '\n ------------------------------------------------------------')
